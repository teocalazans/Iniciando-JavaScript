// tipo de função 

//função declarativa

function aula6() {
    console.log("Tudo certo na massa bahia")
}

aula6()

// expressão de função
//com nomeação
var func = function func() {
    console.log("Tudo mais que certo na nossa bahia")
}
func()

//sem nomeação 
var func = function() {
    console.log("Tudo muito mais que certo na nossa bahia")
}
func()

//Arrow-function
var sup = () => {
    console.log("Tudo bastante muito mais que certo na nossa linda bahia")
}
sup()